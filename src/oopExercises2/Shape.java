package oopExercises2;

public class Shape {
	
	protected double xPos;
	protected double yPos;
	protected int Color;
	
	public Shape() {
		
	}
	
	public double getArea() {
		return 1.0;
	}
	
	public int getColor() {
		return Color;
	}
	
	public double getXPosition() {
		return xPos;
	}
	
	public double getYPosition() {
		return yPos;
	}
	
	
	
}
